name ='hello python world'
# 单词首字母大写
print(name.title())
# 字符串全大写
print(name.upper())
# 字符串全小写
print(name.lower())

print("python")
# 制行符
print("\tpython")
# 换行符
print("python\n")

title =" hello python "
print(title)
# 去掉尾部空格
print(title.rstrip())

# 去掉头部空格
print(title.lstrip())

# 去掉两头空格
print(title.strip())